import angr, claripy
from angr import SimProcedure


class Hook_Taint(SimProcedure):
    def run(self):
        return claripy.BVS('kr', 64)


class Hook_Untaint(SimProcedure):
    def run(self):
        return claripy.BVS('h', 64)





































































































































































































































































































































































































































































































































































































































































































































































































































